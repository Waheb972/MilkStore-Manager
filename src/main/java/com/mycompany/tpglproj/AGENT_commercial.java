    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel(); //#
        int selectedRow = jTable1.getSelectedRow();//#
        int selectedColumn = jTable1.getSelectedColumn();//#
        
        Prix_achat.setText(model.getValueAt(selectedRow, selectedColumn).toString());//#
        jType.setText(model.getValueAt(selectedRow , selectedColumn - 3).toString());//#
        Designation.setText(model.getValueAt(selectedRow , selectedColumn - 2).toString());//#
       
        
        String quantity =  (String) jTable1.getValueAt(selectedRow, selectedColumn - 1);
        int quantity1 = Integer.parseInt(quantity); 
        
        String prix =  (String) jTable1.getValueAt(selectedRow, selectedColumn );
        
        float prix1_tva = Float.parseFloat(prix);
        float tva = 0;
        
        tva = tva /100 ;
        
        prix1_tva = prix1_tva + prix1_tva*tva;
        
        
        float prix_utva= prix1_tva/quantity1;
         
        Prix_unit.setText(String.valueOf(prix_utva));
        
       
        
        
        
    }//GEN-LAST:event_jTable1MouseClicked

